//Comparison Operators

// Equal
'3' == 3

//Not Equal
'3' != 4

//Strict Equal
3 === 3

//Strict Not Equal
'3' !== 3

//Greater than
4 > 3 //true
3 > 4 //false

//Greater than or equal to 
4 >= 4 //true
3 >= 4 //false

//less than
3 < 4 //true
3 < 2 //false

//less than or equal
3 <= 3 //true
3 <= 2 //false

//AND 
true && true //true
true && false //false

//OR
true || false //true
false || false // false


